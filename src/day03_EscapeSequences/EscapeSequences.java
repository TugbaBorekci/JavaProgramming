package day03_EscapeSequences;


/*
Escape Sequences:MUST be given with double quote
\n: stars a new line
\t:paragraph space(tab)
\\:single back slash
\":double quote
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("Java \npython \nC#");

        System.out.println("------------------------------------------");

        System.out.println("\tHello Cydeo \nHow are you all today? \nit is nice to see all! \nWhat class do we have next week?");

        System.out.println("------------------------------------------");

        System.out.println("\t Java is Cool Programming Language");

        System.out.println("------------------------------------------");

        System.out.println("/ \\");//in order to print one backward slash we need to give to backward slashes

        System.out.println("------------------------------------------");

        System.out.println("My favorite TV show is \"Game Of Throne\""); // in order to print double quote, we need to use\"


        }
    }

